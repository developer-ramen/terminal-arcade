//! Official color scheme used throughout the application.

/// Type alias for RGB (3-long [u8] array).
pub type RGB = [u8; 3];

/// Soft, warm yellow evoking the feeling of the sun.
pub const SUNGLOW: RGB = [253, 202, 64];

/// Pale white with a soft touch of blue.
pub const GHOST_WHITE: RGB = [254, 249, 255];

/// Very washed out purple thistle color.
pub const THISTLE: RGB = [212, 193, 236];

/// Light indigo color, akin to a purplish sky blue.
pub const TROPICAL_INDIGO: RGB = [159, 159, 237];

/// Blue with lots of purple.
pub const MEDIUM_SLATE_BLUE: RGB = [115, 108, 237];

/// Bright, warm violet.
pub const FRENCH_VIOLET: RGB = [127, 44, 203];
